//while loops

let i = 0
while (i <= 10) {
    // console.log(`value of i is ${i}`);
    i = i + 2
}

let myArray = ["flash","batman","superman"]
let j = 0
while (j < myArray.length) {
    // console.log(`value is ${myArray[j]}`);
    j++
}

//do-while loop
//minimum 1 baar chalegi hi regardless of the condition
let k = 0
do {
    console.log(`score is ${k}`)
    k++
} while (k <= 10);
